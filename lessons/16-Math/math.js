/* ------------------ Objeto Math -------------------- */

/* Menor valor */
console.log('--------- 1 ---------');
console.log(Math.min(1, 5, 7));

/* Máximo valor */
console.log('--------- 2 ---------');
console.log(Math.max(1, 5, 7));

/* Valor redondeado */
console.log('--------- 3 ---------');
console.log(Math.round(7.5));

/* Raíz cuadrada */
console.log('--------- 4 ---------');
console.log(Math.sqrt(81));

/* Potencia */
console.log('--------- 5 ---------');
console.log(Math.pow(2, 3));

/* Random */
/* Dentro de un round para quitar la parte decimal del ramdom.
Del 0 al 10 */
console.log('--------- 6 ---------');
console.log(Math.round(Math.random() * 10));
